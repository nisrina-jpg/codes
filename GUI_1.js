function formatRM(value) {
  return "RM " + Number(value).toLocaleString("en-MY", {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2
  });
}

function getNumber(id) {
  const value = parseFloat(document.getElementById(id).value);
  return isNaN(value) ? 0 : value;
}

function getChecked(id) {
  return document.getElementById(id).checked;
}

function useDeductions() {
  const checked = document.querySelector('input[name="deductionMode"]:checked');
  return checked && checked.value === "with";
}

function getRates() {
  return zakatConfig[stateSelect.value].deductionRates;
}

function hasRate(key) {
  return typeof getRates()[key] === "number";
}

function populateStates() {
  Object.keys(zakatConfig).forEach((stateName) => {
    const option = document.createElement("option");
    option.value = stateName;
    option.textContent = stateName;
    stateSelect.appendChild(option);
  });

  stateSelect.value = "Kedah";
}

function populateYears() {
  const selectedState = stateSelect.value;
  const nisabByYear = zakatConfig[selectedState].nisabByYear || {};
  const years = Object.keys(nisabByYear).sort((a, b) => Number(b) - Number(a));

  yearSelect.innerHTML = "";

  if (years.length === 0) {
    const option = document.createElement("option");
    option.value = "";
    option.textContent = "Tiada data nisab";
    yearSelect.appendChild(option);
    yearSelect.disabled = true;
    return;
  }

  yearSelect.disabled = false;

  years.forEach((year) => {
    const option = document.createElement("option");
    option.value = year;
    option.textContent = year;
    yearSelect.appendChild(option);
  });

  yearSelect.value = years[0];
}

function getNisabForStateYear(stateName, year) {
  const nisabByYear = zakatConfig[stateName]?.nisabByYear || {};
  return typeof nisabByYear[year] === "number" ? nisabByYear[year] : null;
}

function setDisabled(id, disabled) {
  const el = document.getElementById(id);
  el.disabled = disabled;
  el.classList.toggle("input-disabled", disabled);

  if (disabled && el.type === "checkbox") {
    el.checked = false;
  }

  if (disabled && el.type !== "checkbox") {
    el.value = 0;
  }
}

